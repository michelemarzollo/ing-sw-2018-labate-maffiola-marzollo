/**
 * <strong>Just the skeleton of the class, to allow the implementation of classes linked to it</strong>
 */
public class Cell {

    private int value;

    private Colour colour;

    private Die die;

    public int getValue() {
        return value;

    }

    public Colour getColour() {
        return colour;
    }

    public Die getDie() {
        return die;
    }

}
